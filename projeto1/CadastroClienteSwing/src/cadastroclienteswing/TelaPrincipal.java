    private void jTableClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesMouseClicked
        int linhaSelecionada = jTableClientes.getSelectedRow();
        Long _cpf = (Long) jTableClientes.getValueAt(linhaSelecionada, 1);
        Cliente cliente = this.clientesCadastrados.consultar(_cpf);
        setTextInput(cliente);
        this.cadastrar = false;
        jButtonDeletar.setVisible(true);
        jButtonCadastroConsulta.setText("Cadastrar");
        jButtonSalvar.setText("Atualizar");
        jTextFielCPF.setEnabled(false);
                
    }//GEN-LAST:event_jTableClientesMouseClicked

    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        Long _cpf = Long.valueOf(jTextFielCPF.getText());
        clientesCadastrados.excluir(_cpf);   
        this.cadastrar = true;
        jButtonCadastroConsulta.setText("Consultar");
        jTextFielCPF.setEnabled(true);
        updateTable();
        clearTextInput();
        JOptionPane.showMessageDialog(null, "Cliente excluído!");
        jButtonDeletar.setVisible(false);
    }//GEN-LAST:event_jButtonDeletarActionPerformed

