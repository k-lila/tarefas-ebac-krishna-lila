    public Boolean isInputStringValid(String input, Integer length) {
        return input.length() >= length;
    }
    
    public Boolean isInputLongValid(String input, Integer length) {
        return input.length() >= length && input.matches("\\d+");
    }
    
    public Boolean isInputIntegerValid(String input) {
        return input.matches("\\d+");
    }
    
    public void clearTextInput() {
        jTextFieldNome.setText("");
        if (this.cadastrar) {jTextFielCPF.setText("");}
        jTextFieldTel.setText("");
        jTextFieldEnd.setText("");
        jTextFieldNum.setText("");
        jTextFieldCidade.setText("");
        jTextFieldEstado.setText("");
    }
    
    public void setTextInput(Cliente cliente) {
        jTextFieldNome.setText(cliente.getNome() == null ? "" : cliente.getNome());
        jTextFielCPF.setText(cliente.getCpf().toString());
        jTextFieldTel.setText(cliente.getTel() == null ? "" : cliente.getTel().toString());
        jTextFieldEnd.setText(cliente.getEnd() == null ? "" : cliente.getEnd());
        jTextFieldNum.setText(cliente.getNumero() == null ? "" : cliente.getNumero().toString());
        jTextFieldCidade.setText(cliente.getCidade() == null ? "" : cliente.getCidade());
        jTextFieldEstado.setText(cliente.getCidade() == null ? "" : cliente.getEstado());
    }    
    
    public void updateTable() {
        this.modelo.setRowCount(0);
        for (Cliente cliente : this.clientesCadastrados.buscarTodos()) {
            this.modelo.addRow(new Object[] {cliente.getNome(), cliente.getCpf()});
        }
    }
    
    private void jButtonCadastroConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastroConsultaActionPerformed
        if (!cadastrar) {
            jTextFielCPF.setEnabled(true);
            this.cadastrar = !this.cadastrar;
            clearTextInput();
            jButtonDeletar.setVisible(false);
            jButtonCadastroConsulta.setText("Consultar");
            jButtonSalvar.setText("Salvar");
        } else {
            String inputString = JOptionPane.showInputDialog(null, "Digite o CPF:", "Consultar", JOptionPane.INFORMATION_MESSAGE);
            if (inputString != null) {
                if (isInputLongValid(inputString, 1)) {
                    Cliente clienteBuscado = this.clientesCadastrados.consultar(Long.valueOf(inputString));
                    if (clienteBuscado != null) {
                        setTextInput(clienteBuscado);
                        this.cadastrar = !this.cadastrar;
                        jTextFielCPF.setEnabled(false);
                        jButtonDeletar.setVisible(true);
                        jButtonCadastroConsulta.setText("Cadastrar");
                        jButtonSalvar.setText("Atualizar");
                    } else {
                        JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                    }        
                } else {
                    JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                }
            }            
        }
    }//GEN-LAST:event_jButtonCadastroConsultaActionPerformed
    
    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Cliente novoCliente = new Cliente(null, null, null, null, null, null, null);
        String _nome = jTextFieldNome.getText();
        String _cpf = jTextFielCPF.getText();
        String _tel = jTextFieldTel.getText();
        String _end = jTextFieldEnd.getText();
        String _num = jTextFieldNum.getText();
        String _cidade = jTextFieldCidade.getText();
        String _estado = jTextFieldEstado.getText();
        String erros = "";
        if (isInputStringValid(_nome, 3)) { 
            novoCliente.setNome(_nome);
        } else {erros += "O nome deve ter ao menos 3 caracteres\n";}
        
        if (isInputLongValid(_cpf, 1)) {
            novoCliente.setCpf(Long.valueOf(_cpf)); 
        } else {erros += "cpf inválido\n";}
        if (isInputLongValid(_tel, 1)) { novoCliente.setTel(Long.valueOf(_tel)); }     
        if (isInputStringValid(_end, 1)) { novoCliente.setEnd(_end); }
        if (isInputIntegerValid(_num)) {novoCliente.setNumero(Integer.valueOf(_num));}
        if (isInputStringValid(_cidade, 1)) { novoCliente.setCidade(_cidade); }
        if (isInputStringValid(_estado, 1)) { novoCliente.setEstado(_estado); }
        
        if (this.cadastrar) {
            if (isInputLongValid(_cpf, 1) && (isInputStringValid(_nome, 3))) {
                if (clientesCadastrados.consultar(Long.valueOf(_cpf)) == null) {
                    this.clientesCadastrados.cadastrar(novoCliente);
                    updateTable();
                    JOptionPane.showMessageDialog(null, "Cliente cadastrado!");
                    clearTextInput();
                } else {
                    JOptionPane.showMessageDialog(null, "Cliente já cadastrado!");
                    clearTextInput();
                }
            } else {
                JOptionPane.showMessageDialog(null, erros);
            }
        } else {
            if (isInputStringValid(_nome, 3)) {
                clientesCadastrados.alterar(novoCliente);
                updateTable();
                JOptionPane.showMessageDialog(null, "Informações alteradas!");
            } else {
                JOptionPane.showMessageDialog(null, erros);
            }

        }               
    }//GEN-LAST:event_jButtonSalvarActionPerformed

